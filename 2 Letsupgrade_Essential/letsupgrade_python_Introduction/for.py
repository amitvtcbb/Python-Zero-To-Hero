# Python for loop

# used for iterate over a sequence - list, tuple, string
# iterative over a sequence is called for loop

# syntax:

# for element in sequence:
    # Body of for


for i in range(3):
    print("Line 1")
    print("Line 2")

for i in "hello":
    print(i)

# for is a keyword define "for"
# i - iterative
# in - another keyword
# last part - range(start=0, step, end=3)